async function insertChapterSummaries() {
  console.log("=== 시가서 + 대선지서 장별 요약 입력 시작 ===\n");

  const bookOrder = [
    "job", "psalms", "proverbs", "ecclesiastes", "songofsolomon",
    "isaiah", "jeremiah_book", "lamentations", "ezekiel", "daniel"
  ];

  const bookNames = {
    job: "욥기", psalms: "시편", proverbs: "잠언",
    ecclesiastes: "전도서", songofsolomon: "아가",
    isaiah: "이사야", jeremiah_book: "예레미야",
    lamentations: "예레미야애가", ezekiel: "에스겔", daniel: "다니엘"
  };

  let totalInserted = 0;
  let totalErrors = 0;

  for (const bookId of bookOrder) {
    const summaries = chapterSummaries[bookId];
    const bookName = bookNames[bookId];
    console.log(`📖 ${bookName} (${bookId}) — ${summaries.length}장`);

    let bookInserted = 0;
    // Process in batches of 20 to avoid overwhelming the connection
    const batchSize = 20;
    for (let i = 0; i < summaries.length; i += batchSize) {
      const batch = summaries.slice(i, i + batchSize);
      const promises = batch.map((summary, idx) => {
        const chapter = i + idx + 1;
        return db.execute(
          `INSERT INTO book_chapters (book_id, chapter, summary) VALUES (?, ?, ?) ON DUPLICATE KEY UPDATE summary=VALUES(summary)`,
          [bookId, chapter, summary]
        ).then(() => {
          bookInserted++;
          return true;
        }).catch((err) => {
          console.error(`  ❌ ${bookName} ${chapter}장 실패: ${err.message}`);
          totalErrors++;
          return false;
        });
      });
      await Promise.all(promises);
    }

    console.log(`  ✅ ${bookInserted}/${summaries.length}장 완료\n`);
    totalInserted += bookInserted;
  }

  console.log("=== 결과 ===");
  console.log(`총 입력: ${totalInserted}장`);
  console.log(`총 오류: ${totalErrors}건`);

  // Verify
  const result = await db.execute(
    `SELECT book_id, COUNT(*) as cnt FROM book_chapters WHERE book_id IN (?, ?, ?, ?, ?, ?, ?, ?, ?, ?) GROUP BY book_id ORDER BY book_id`,
    ["job", "psalms", "proverbs", "ecclesiastes", "songofsolomon",
     "isaiah", "jeremiah_book", "lamentations", "ezekiel", "daniel"]
  );
  console.log("\n=== DB 검증 ===");
  console.log(JSON.stringify(result, null, 2));
}

