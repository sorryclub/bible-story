async function insertChapters() {
  console.log(`Inserting ${chapters.length} chapter summaries...\n`);

  const sql = `INSERT INTO book_chapters (book_id, chapter, summary) VALUES (?, ?, ?) ON DUPLICATE KEY UPDATE summary=VALUES(summary)`;

  let success = 0;
  let errors = 0;

  for (const [bookId, chapter, summary] of chapters) {
    try {
      await db.execute(sql, [bookId, chapter, summary]);
      success++;
      if (success % 20 === 0) {
        console.log(`  ... ${success} rows inserted`);
      }
    } catch (err) {
      errors++;
      console.error(`  ERROR [${bookId} ${chapter}]: ${err.message}`);
    }
  }

  console.log(`\nDone! Success: ${success}, Errors: ${errors}`);
  console.log(`Total chapters processed: ${chapters.length}`);

  // Verify counts
  const result = await db.execute(
    `SELECT book_id, COUNT(*) as cnt FROM book_chapters WHERE book_id IN ('genesis','exodus','leviticus','numbers','deuteronomy') GROUP BY book_id ORDER BY FIELD(book_id,'genesis','exodus','leviticus','numbers','deuteronomy')`
  );
  console.log("\nVerification:");
  for (const row of result) {
    console.log(`  ${row.book_id}: ${row.cnt} chapters`);
  }
}

insertChapters().catch(console.error);
