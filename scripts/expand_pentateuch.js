async function updateChapters() {
  console.log(`Updating ${chapters.length} chapter summaries...\n`);

  const sql = `UPDATE book_chapters SET summary = ? WHERE book_id = ? AND chapter = ?`;

  let success = 0;
  let errors = 0;

  for (const [bookId, chapter, summary] of chapters) {
    try {
      const result = await db.execute(sql, [summary, bookId, chapter]);
      success++;
      if (success % 20 === 0) {
        console.log(`  ... ${success} rows updated`);
      }
    } catch (err) {
      errors++;
      console.error(`  ERROR [${bookId} ${chapter}]: ${err.message}`);
    }
  }

  console.log(`\nDone! Success: ${success}, Errors: ${errors}`);
  console.log(`Total chapters processed: ${chapters.length}`);

  // Verify by sampling
  console.log("\n=== Verification: Sample summaries ===");
  const samples = [
    ["genesis", 1], ["genesis", 22], ["genesis", 50],
    ["exodus", 3], ["exodus", 14], ["exodus", 40],
    ["leviticus", 1], ["leviticus", 16], ["leviticus", 27],
    ["numbers", 1], ["numbers", 21], ["numbers", 36],
    ["deuteronomy", 1], ["deuteronomy", 6], ["deuteronomy", 34],
  ];

  for (const [bookId, ch] of samples) {
    const rows = await db.execute(
      `SELECT book_id, chapter, summary, CHAR_LENGTH(summary) as len FROM book_chapters WHERE book_id = ? AND chapter = ?`,
      [bookId, ch]
    );
    if (rows.length > 0) {
      const r = rows[0];
      console.log(`  [${r.book_id} ${r.chapter}] (${r.len}자) ${r.summary}`);
    }
  }

  // Count per book
  console.log("\n=== Chapter counts per book ===");
  const counts = await db.execute(
    `SELECT book_id, COUNT(*) as cnt FROM book_chapters WHERE book_id IN ('genesis','exodus','leviticus','numbers','deuteronomy') GROUP BY book_id ORDER BY FIELD(book_id,'genesis','exodus','leviticus','numbers','deuteronomy')`
  );
  for (const row of counts) {
    console.log(`  ${row.book_id}: ${row.cnt} chapters`);
  }
}

updateChapters().catch(console.error);
