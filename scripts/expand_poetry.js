async function updateChapterSummaries() {
  console.log("=== 시가서 + 대선지서 상세 요약 UPDATE 시작 ===\n");

  const bookOrder = [
    "job", "psalms", "proverbs", "ecclesiastes", "songofsolomon",
    "isaiah", "jeremiah_book", "lamentations", "ezekiel", "daniel"
  ];

  const bookNames = {
    job: "욥기", psalms: "시편", proverbs: "잠언",
    ecclesiastes: "전도서", songofsolomon: "아가",
    isaiah: "이사야", jeremiah_book: "예레미야",
    lamentations: "예레미야애가", ezekiel: "에스겔", daniel: "다니엘"
  };

  let totalUpdated = 0;
  let totalErrors = 0;

  for (const bookId of bookOrder) {
    const summaries = expandedSummaries[bookId];
    const bookName = bookNames[bookId];
    console.log(`[${bookName}] (${bookId}) — ${summaries.length}장 UPDATE 시작`);

    let bookUpdated = 0;
    const batchSize = 15;

    for (let i = 0; i < summaries.length; i += batchSize) {
      const batch = summaries.slice(i, i + batchSize);
      const promises = batch.map((summary, idx) => {
        const chapter = i + idx + 1;
        return db.execute(
          `UPDATE book_chapters SET summary = ? WHERE book_id = ? AND chapter = ?`,
          [summary, bookId, chapter]
        ).then((result) => {
          bookUpdated++;
          return true;
        }).catch((err) => {
          console.error(`  [ERROR] ${bookName} ${chapter}장: ${err.message}`);
          totalErrors++;
          return false;
        });
      });
      await Promise.all(promises);
    }

    console.log(`  => ${bookUpdated}/${summaries.length}장 완료\n`);
    totalUpdated += bookUpdated;
  }

  console.log("=== 결과 ===");
  console.log(`총 UPDATE: ${totalUpdated}장`);
  console.log(`총 오류: ${totalErrors}건`);

  // Verify with sample checks
  console.log("\n=== 샘플 검증 ===");
  const samples = [
    { book: "psalms", ch: 23, name: "시편 23편" },
    { book: "isaiah", ch: 53, name: "이사야 53장" },
    { book: "proverbs", ch: 3, name: "잠언 3장" },
    { book: "job", ch: 42, name: "욥기 42장" },
    { book: "daniel", ch: 6, name: "다니엘 6장" },
  ];

  for (const s of samples) {
    const row = await db.execute(
      `SELECT summary, CHAR_LENGTH(summary) as len FROM book_chapters WHERE book_id = ? AND chapter = ?`,
      [s.book, s.ch]
    );
    if (row && row.length > 0) {
      console.log(`\n[${s.name}] (${row[0].len}자)`);
      console.log(`  "${row[0].summary}"`);
    }
  }

  // Count summary lengths
  console.log("\n=== 글자 수 통계 ===");
  const stats = await db.execute(
    `SELECT book_id,
            MIN(CHAR_LENGTH(summary)) as min_len,
            MAX(CHAR_LENGTH(summary)) as max_len,
            ROUND(AVG(CHAR_LENGTH(summary))) as avg_len,
            COUNT(*) as cnt
     FROM book_chapters
     WHERE book_id IN ('job','psalms','proverbs','ecclesiastes','songofsolomon','isaiah','jeremiah_book','lamentations','ezekiel','daniel')
     GROUP BY book_id
     ORDER BY FIELD(book_id, 'job','psalms','proverbs','ecclesiastes','songofsolomon','isaiah','jeremiah_book','lamentations','ezekiel','daniel')`
  );
  console.table(stats);
}

