async function main() {
  console.log(`총 ${chapters.length}개 장 요약 삽입 시작...\n`);

  let success = 0;
  let fail = 0;

  // Process in batches of 10 to avoid overwhelming the connection
  const batchSize = 10;
  for (let i = 0; i < chapters.length; i += batchSize) {
    const batch = chapters.slice(i, i + batchSize);
    const promises = batch.map(([bookId, chapter, summary]) =>
      db.execute(
        'INSERT INTO book_chapters (book_id, chapter, summary) VALUES (?, ?, ?) ON DUPLICATE KEY UPDATE summary=VALUES(summary)',
        [bookId, chapter, summary]
      )
        .then(() => {
          success++;
          return { bookId, chapter, ok: true };
        })
        .catch((err) => {
          fail++;
          console.error(`  [ERROR] ${bookId} ${chapter}장: ${err.message}`);
          return { bookId, chapter, ok: false };
        })
    );
    const results = await Promise.all(promises);
    const batchEnd = Math.min(i + batchSize, chapters.length);
    console.log(`  [${batchEnd}/${chapters.length}] 처리 완료`);
  }

  console.log(`\n완료: 성공 ${success}, 실패 ${fail} (총 ${chapters.length})`);
}

main().catch(console.error);
